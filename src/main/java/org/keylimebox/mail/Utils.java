         /*=============================================================================*/
         /* OPERATION:   nullAsBlank                                                    */
         /**
          * Returns the given string or blank (empty string) if it is <code>null</code>.
          *
          * <p>
          * @param aString
          *         The string.
          * <p>
          * @return <code>String</code> -
          *          The string or blank, but not <code>null</code>.
          */
         /*=============================================================================*/
   public static final String nullAsBlank (
                                           String  aString
                                           )
   {
      return ((aString == null || aString.trim ().equalsIgnoreCase ("null")) ? BLANK_STRING : aString);
   }

